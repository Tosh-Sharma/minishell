#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!del)
		return ;
	tmp = lst;
	if (tmp)
	{
		(*del)(tmp->content);
		free(tmp);
	}
}
